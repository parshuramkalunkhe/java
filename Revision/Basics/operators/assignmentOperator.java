class assignmentOperator {
	public static void main(String[] args) {
		int a = 10; // = --> assignment operator.
		System.out.println("Output of assignment operator: " + a);
		a += 10;	// += --> addition & assignment operator.
		System.out.println("Output of addition & assignment operator: " + a);
		a -= 10; // -= --> substraction & assignment operator.
		System.out.println("Output of substraction & assignment operator: " + a);
		a *= 10; // *= --> multiplication & assignment operator.
		System.out.println("Output of multiplication & assignment operator: " + a);
		a /= 10; // /= --> division & assignment operator.
		System.out.println("Output of division & assignment operator: " + a);
		a %= 10; // %= --> remainder & assignment operator.
		System.out.println("Output of remainder & assignment operator: " + a);
	}
}
