class relationalOperator {
	public static void main(String[] args) {
		// used to compare two values
		int a = 10, b = 20;
		System.out.println("Comparing two values. a: " + a + " b:" + b);
		System.out.println("Greater than (a > b): " + (a > b));
		System.out.println("Less than (a < b): " + (a < b));
		System.out.println("Greater than equals (a >= b): " + (a >= b));
		System.out.println("Less than equals (a <= b): " + (a <= b));
		System.out.println("Not equals (a != b): " + (a != b));
		System.out.println("equals (a == b): " + (a == b));

	}
}
